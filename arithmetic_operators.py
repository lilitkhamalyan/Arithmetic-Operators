# Get numbers from the user
n1 = int(input("Enter the first number: "))
n2 = int(input("Enter the second number: "))

# Calculate the sum of the two numbers and display
sumOfNumbers = n1 + n2
print("The sum of the two numbers is:", sumOfNumbers)

# Calculate the difference of the two numbers and display
difOfNumbers = n1 - n2
print("The difference of the two numbers(first - second) is:", difOfNumbers)

# Calculate the product of the two numbers and display
productOfNumbers = n1 * n2
print("The product of the two numbers is:", productOfNumbers)
